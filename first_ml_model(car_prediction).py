#import reqrd Libraries
import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, r2_score
import joblib # for saving the model

# Loading the data set
data=pd.read_csv("/content/Linear-Regression-_Training-Data_-.csv")

# To check the data set in tubular form
data.head()

# Define independent variable(X) and dependent variable (y)
X=data[["Age","Education","Income","Vehicle Ownership"]]
y=data["Purchase hybrid vehicle"]

# Initialise & train the linear regression model
model=LinearRegression()
model.fit(X,y)

# Save the trained model to file for later use
joblib.dump(model,"hybrid_vehicle_prediction_model.pkl")
print("model has been trained and saved successfully ")

loaded_model=joblib.load("hybrid_vehicle_prediction_model.pkl")

data_new=pd.read_csv("/content/Linear-Regression-Python-_Test-data_.csv")

data_new.head()

# make predictions using the model on the new input data (data_new)
data_new["prediction % "]=((loaded_model.predict(data_new))*100).round(2)

data_new.head()

# Download the csv file
data_new.to_excel("Samraat_Prediction.xlsx", index=False)